//Напишите функцию canonizePath, которая принимает абсолютный путь в Unix -
//подобной операционной системе и преобразует его к канонической форме.

// Правила преобразования:
// .. - означает переход на один уровень выше в иерархии директорий
// . - означает текущий уровень в иерархии директорий
// Несколько слэшей // подряд превращаются в один /
// Путь не должен оканчиваться слэшем(кроме корневого пути /)
// Нельзя подняться выше корневой директории(лишние .. игнорируются)

// Вход: "/dir/subdir/../file.txt"
// Выход: "/dir/file.txt"

// Вход: "/dir/subdir/../../../file.txt"
// Выход: "/file.txt"

// Вход: "/dir/.."
// Выход: "/"

// Вход: "/"
// Выход: "/"

// ответ
function canonizePath(path) {
  if (path === "/") return "/";

  const folders = path.split("/");
  const stackFolders = [];
  for (let i = 0; i < folders.length; i++) {
    if (folders[i] !== "..") {
      stackFolders.push(folders[i]);
    } else {
      if (stackFolders.length > 0) {
        stackFolders.pop();
      }
    }
  }
  const result = "/" + stackFolders.join("/");
  return result.replace(/\/+/g, "/");
}

console.log(
  "мой ответ:",
  canonizePath("/dir/subdir/../file.txt"),
  "должен быть",
  "/dir/file.txt",
); // "/dir/file.txt"
console.log(
  "мой ответ:",
  canonizePath("/dir/subdir/../../../file.txt"),
  "должен быть",
  "/file.txt",
); // "/file.txt"
console.log("мой ответ:", canonizePath("/dir/.."), "должен быть", "/"); // "/"
console.log("мой ответ:", canonizePath("/"), "должен быть", "/"); // "/"

