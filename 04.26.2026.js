// Напишите функцию solve, которая принимает массив чисел и возвращает массив из доминантных элементов.
// Доминантным является элемент массива, который больше, чем все элементы, следующие за ним.

// Правила:
// Элемент считается доминантным, если он больше всех элементов справа от него
// Последний элемент всегда доминантный(справа от него нет элементов)
// Порядок элементов в результате должен соответствовать порядку в исходном массиве

// Примеры:

// solve([16, 17, 14, 3, 14, 5, 2])  // -> [17, 14, 5, 2]
// solve([92, 52, 93, 31, 89, 87, 77, 105])  // -> [105]
// solve([75, 47, 42, 56, 13, 55])  // -> [75, 56, 55]

function solve(array) {
  const arrMax = [array.at(-1)];
  let maxElement = array.at(-1);

  // return [arrMax, maxElement]

  for (let i = array.length - 2; 0 <= i; i = i - 1) {
    if (array[i] > maxElement) {
      arrMax.push(array[i]);
      maxElement = array[i];
    }
  }

  return arrMax.reverse();
}

// arrMax = [55]
// maxElement = 55

// 13 > 55

// 56 > 55
// arrMax = [55, 56]
// maxElement = 56

// 42 > 56

// 47 > 56

// 75 > 56
// arrMax = [55, 56, 75]
// maxElement = 75

// [75, 56, 55]

console.log(solve([75, 47, 42, 56, 13, 55])); // -> [75, 56, 55]
console.log(solve([16, 17, 14, 3, 14, 5, 2])); // -> [17, 14, 5, 2]
console.log(solve([92, 52, 93, 31, 89, 87, 77, 105])); // -> [105]
